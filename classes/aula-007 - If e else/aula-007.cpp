// Usando IF e ELSE

#include <iostream>
using namespace std;

int main() {
	
	int num1 = 10;
	int num2 = 50;
	
	/*
		OPERADORES:
			> = maior
			< = menor
			>= = maior ou igual
			<= = menor ou igual
			== = igual
			!= = diferente
	*/
	
	if (num1 < num2) {
		// Se o teste for verdadeiro ele executa este bloco de código
		
		cout << "Resultado VERDADEIRO";
		
	} else {
		// Se o teste for falso ele executa este bloco de código
		
		cout << "Resultado FALSO";
	};
	
	return 0;
}


